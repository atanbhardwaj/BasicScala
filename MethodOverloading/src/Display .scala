

class Display  {
  def disp(x:Int){
    
    println("The square of " + "is : "+ x*x)
   
  }
  
  def disp(x:Int, y:Double){
    println(x+" "+y)
  }
  
  def disp(x:Int, y:Double, z:String){
    println(x+" "+y+" "+z)
   
  }
  
  def disp(x:Int, y:Double, z:String, k:Boolean){
    println(x+" "+y+" "+z+" "+k)
  }
  
}